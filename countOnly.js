const countOnly = function(allItems, itemsToCount) { // allItems = arr of str's / itemsToCount = object specifying what to count
  const results = {};
  for (const item of allItems) {
    if (itemsToCount[item]) {
      if (results[item]) {
        results[item] += 1;
      } else {
        results[item] = 1;
      }
    }
  }
  return results;
};

module.exports = countOnly;
